package com.bs.entity;

public class AllFeedInformation {
	private Integer feedId;
	private String feedName;
	private Integer feedCategory;
	private String feedIntroduction;
	private String remarks;
	private String supplier;
	private String stock;
	private String fomulaId;
	public Integer getFeedId() {
		return feedId;
	}
	public void setFeedId(Integer feedId) {
		this.feedId = feedId;
	}
	public String getFeedName() {
		return feedName;
	}
	public void setFeedName(String feedName) {
		this.feedName = feedName;
	}
	public Integer getFeedCategory() {
		return feedCategory;
	}
	public void setFeedCategory(Integer feedCategory) {
		this.feedCategory = feedCategory;
	}
	public String getFeedIntroduction() {
		return feedIntroduction;
	}
	public void setFeedIntroduction(String feedIntroduction) {
		this.feedIntroduction = feedIntroduction;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public String getSupplier() {
		return supplier;
	}
	public void setSupplier(String supplier) {
		this.supplier = supplier;
	}
	public String getStock() {
		return stock;
	}
	public void setStock(String stock) {
		this.stock = stock;
	}
	public String getFomulaId() {
		return fomulaId;
	}
	public void setFomulaId(String fomulaId) {
		this.fomulaId = fomulaId;
	}
	
}
